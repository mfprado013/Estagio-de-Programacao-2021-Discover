//Operadores Logicos (logical operators)

// - 2 valores booleanos, quando verificados,
// resultara em verdadeiro ou falso

let pao = true
let queijo = true

//AND && - aqui temos que ter as duas coisas true ou false
console.log(pao && queijo)

//OR || - aqui temso um outro ou outro vai funcionar se não tiver os dois não funciona 
console.log(pao || queijo)

//NOT ! - aqui sempre sera negado se colocar ! 
console.log(!pao)
